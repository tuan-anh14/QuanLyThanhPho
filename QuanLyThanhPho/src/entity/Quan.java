package entity;

public class Quan {
    private int quanId;
    private String tenQuan;
    private int soLuongNguoi;
    private int soLuongDuong;
    private int soLuongCongTrinh;

    // Getters and Setters
    public int getQuanId() {
        return quanId;
    }

    public void setQuanId(int quanId) {
        this.quanId = quanId;
    }

    public String getTenQuan() {
        return tenQuan;
    }

    public void setTenQuan(String tenQuan) {
        this.tenQuan = tenQuan;
    }

    public int getSoLuongNguoi() {
        return soLuongNguoi;
    }

    public void setSoLuongNguoi(int soLuongNguoi) {
        this.soLuongNguoi = soLuongNguoi;
    }

    public int getSoLuongDuong() {
        return soLuongDuong;
    }

    public void setSoLuongDuong(int soLuongDuong) {
        this.soLuongDuong = soLuongDuong;
    }

    public int getSoLuongCongTrinh() {
        return soLuongCongTrinh;
    }

    public void setSoLuongCongTrinh(int soLuongCongTrinh) {
        this.soLuongCongTrinh = soLuongCongTrinh;
    }
}
